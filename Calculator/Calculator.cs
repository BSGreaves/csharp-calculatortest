using System;

namespace Calculator
{
    public class Calc
    {
        public Calc(){}
        public int AddTwoIntegers (int x, int y)
        {
            return x + y;
        }
        public int SubtractAnInteger (int x, int y)
        {
            return x - y;
        }
        public int MultiplyTwoIntegers (int x, int y)
        {
            return x * y;
        }
        public int DivideAnInteger (int x, int y)
        {
            return x / y;
        }
    }

}